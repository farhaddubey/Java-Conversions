// Char can be converted to String using either String.valueOf(char) method or using Character.toString(char) method 
// char c='S'; 
// String s=String.valueOf(c); 

public class CharToString{
    public static void main(String[] args){
        char c='S';
        String s=String.valueOf(c);
        System.out.println("String is: "+s);
    }
}

