// Long can be converted to int using typecasting. 
// To convert higher datatype into lower we have to perform typecasting. args

public class LongToInt{
    public static void main(String[] args){
        long l=500; 
        int i=(int) l; 
        // Assigning to int i 
        // typecasting () by mentioning the type explicitly inside 1st braces 
        System.out.println(i);
    }
}